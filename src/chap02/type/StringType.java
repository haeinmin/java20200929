package chap02.type;

public class StringType {
	public static void main(String[] args) {
		// String: 문자열 저장
		String carName;
		carName = "Kia";
		
		System.out.println(carName);
		System.out.println(""); //char와 달리 string은 0개 이상 문자열 저장 가능
		System.out.println("여러문자열...");
		System.out.println("큰따옴표\" 작은따옴표'");
	}

}
